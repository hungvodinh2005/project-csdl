    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
       String patientId = maBn.getText().trim();
    String idCard = cccd.getText().trim();
    String maBhytValue = bhyt.getText().trim();
    
    // Lấy giá trị từ combo "mã - tên"
    String guardianItem = (String) giamHo.getSelectedItem();
    String guardianId = null;
    if (guardianItem != null && !guardianItem.isEmpty()) {
        guardianId = guardianItem.split(" - ")[0]; // chỉ lấy mã
    }

    // Kiểm tra dữ liệu bắt buộc
    if (patientId.isEmpty() || idCard.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mã bệnh nhân và CCCD không được để trống!");
        return;
    }

    // Tạo đối tượng Patient
    Patient p = new Patient();
    p.setPatientId(patientId);
    p.setIdCard(idCard);
    p.setMaBhyt(maBhytValue);
    p.setGuardianId(guardianId);

    // Gọi controller update
    PatientController pc = new PatientController();
    boolean success = pc.updatePatient(p);

    if (success) {
        javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật bệnh nhân thành công!");
        showTableActionPerformed(null); // refresh bảng
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
    }
    }//GEN-LAST:event_editActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
      String patientId = maBn.getText().trim();

    if (patientId.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn bệnh nhân để xóa!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "Bạn có chắc muốn xóa bệnh nhân này?", 
            "Xác nhận", 
            javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        PatientController pc = new PatientController();
        boolean success = pc.deletePatient(patientId);

        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa bệnh nhân thành công!");
            showTableActionPerformed(null); // refresh bảng
            // Xóa dữ liệu trên form
            maBn.setText("");
            cccd.setText("");
            bhyt.setText("");
            giamHo.setSelectedIndex(-1);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    }
    }//GEN-LAST:event_deleteActionPerformed

    private void showTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showTableActionPerformed
    List<Patient> patients = new PatientController().getAllPatients();

    // Lấy model JTable
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0); // Xóa dữ liệu cũ

    PatientController patientCtrl = new PatientController(); // Controller chứa hàm getGuardianName

    // Đổ dữ liệu vào bảng
    for (Patient p : patients) {
        // Lấy họ tên bệnh nhân từ CCCD
        String hoTen = patientCtrl.searchPatient(p.getPatientId());

        // Lấy tên người giám hộ (nếu có)
        String guardianDisplay = "";
        String guardianId = p.getGuardianId();
        if (guardianId != null && !guardianId.isEmpty()) {
            String guardianName = patientCtrl.getGuardianName(guardianId);
            guardianDisplay = guardianId + " - " + (guardianName != null ? guardianName : "");
        }

        // Thêm dòng vào bảng
        model.addRow(new Object[]{
            p.getPatientId(),   // Mã bệnh nhân
            p.getIdCard(),      // CCCD
            hoTen,              // Họ tên
            p.getMaBhyt(),
            guardianDisplay     // Người giám hộ dạng "mã - tên"
        });
    }
    }//GEN-LAST:event_showTableActionPerformed

