    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int selectedRow = table.getSelectedRow();
    if (selectedRow >= 0) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        // Đổ dữ liệu từ JTable về các ô JTextField
        maBn.setText(model.getValueAt(selectedRow, 0).toString()); // Mã bệnh nhân
        cccd.setText(model.getValueAt(selectedRow, 1).toString()); // CCCD
        bhyt.setText(model.getValueAt(selectedRow, 3).toString()); // Mã BHYT

        // Người giám hộ
        String guardianDisplay = model.getValueAt(selectedRow, 4).toString(); // "GH001 - Nguyễn Văn A"

        // Duyệt tất cả item trong combo và chọn đúng item
        for (int i = 0; i < giamHo.getItemCount(); i++) {
            if (giamHo.getItemAt(i).equals(guardianDisplay)) {
                giamHo.setSelectedIndex(i);
                break;
            }
        }
    }
    }//GEN-LAST:event_tableMouseClicked
    public  void loadGuardiansToCombo() {
        // Xóa dữ liệu cũ
        giamHo.removeAllItems();

        // Lấy danh sách người giám hộ
        PatientController pgc = new PatientController();
        for (Guardian g : pgc.getAllGuardians()) {
            giamHo.addItem(g.toString()); // Thêm dạng "mã - tên"
        }
    }

